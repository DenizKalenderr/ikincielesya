
public class Esya {

private int id;
private String ad;
private String fiyat;
private String adet;

    public Esya(int id, String ad, String fiyat, String adet) {
        this.id = id;
        this.ad = ad;
        this.fiyat = fiyat;
        this.adet = adet;
    }

   

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getAd() {
        return ad;
    }

    public void setAd(String ad) {
        this.ad = ad;
    }

    public String getFiyat() {
        return fiyat;
    }

    public String setFiyat(String fiyat) {
        return fiyat;
    }

    public String getAdet() {
        return adet;
    }

    public String setAdet(String adet) {
        return adet;
    }

    
}
